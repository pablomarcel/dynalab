"""simulator.ui.inspector.inspector_panel

A parameter inspector for the selected block.

What this version fixes/implements (MVP -> usable):
- Specialized editors for the blocks you actually need to do control work:
    * TF: numerator/denominator editor
    * Gain: scalar gain editor
    * Sum: n_inputs + per-input sign (+/-) editor
    * Step: amplitude + t0
    * Scope: label
  Everything else falls back to a generic key/value editor.

- Validation on Apply using the BlockRegistry spec's validate_params().
  Errors are shown inline and Apply is blocked until fixed.

Contract:
- Emits sig_block_updated(block_id, params_dict)
  (keeps existing MainWindow wiring unchanged).

Notes:
- Sum signs are stored in params["signs"] and params["n_inputs"]. The IR ports' sign
  values are regenerated by registry spec default_block() during graph_to_ir().

Updated:
- Treats ("tf", "tf_flipped") the same, so TF (Flipped) reuses the Num/Den editor.
- Treats ("sum", "sum_glyph") the same, so the Sum Glyph reuses the same n_inputs/sign editor.
- UI polish only: adds header, section group boxes, spacing, and a compact error banner.
  (No behavior changes to data collection/validation.)
"""

from __future__ import annotations

